SEO_TEMPLATE = """
<section class="tool-seo">
  <div class="seo-inner">
    <div class="seo-block">
      <div class="seo-h2">📌 ツールの概要・特徴</div>
      <p class="seo-p">{overview}</p>
    </div>
    <div class="seo-block">
      <div class="seo-h2">📖 詳しい使い方・手順</div>
      <ol class="seo-ol">
{steps}
      </ol>
    </div>
    <div class="seo-block">
      <div class="seo-h2">👤 こんな人におすすめ</div>
      <p class="seo-p">{recommend}</p>
    </div>
    <div class="seo-block">
      <div class="seo-h2">🛠️ 制作の背景・こだわり</div>
      <p class="seo-p">{background}</p>
    </div>
  </div>
</section>"""

def format_steps(steps):
    return "\n".join(f"        <li>{s}</li>" for s in steps)

def build_seo_block(data):
    steps_html = format_steps(data["steps"])
    return SEO_TEMPLATE.format(
        overview=data["overview"],
        steps=steps_html,
        recommend=data["recommend"],
        background=data["background"],
    )

def process_file(filename, data):
    filepath = os.path.join(TOOLS_DIR, filename + ".astro")
    if not os.path.exists(filepath):
        print(f"SKIP (not found): {filepath}")
        return False

    with open(filepath, "r", encoding="utf-8") as f:
        content = f.read()

    # Already has SEO content?
    if "tool-seo" in content:
        print(f"SKIP (already has SEO): {filename}")
        return False

    seo_block = build_seo_block(data)

    # Find </BaseLayout> or </Layout>
    # Pattern detection: find the FIRST closing tag of layout
    baselayout_pos = content.find("</BaseLayout>")
    layout_pos = content.find("</Layout>")

    if baselayout_pos == -1 and layout_pos == -1:
        print(f"ERROR (no layout closing tag): {filename}")
        return False

    # Determine which layout tag to use
    if baselayout_pos != -1 and (layout_pos == -1 or baselayout_pos < layout_pos):
        tag = "</BaseLayout>"
        pos = baselayout_pos
    else:
        tag = "</Layout>"
        pos = layout_pos

    # Insert SEO block just before the layout closing tag
    new_content = content[:pos] + seo_block + "\n" + content[pos:]

    with open(filepath, "w", encoding="utf-8") as f:
        f.write(new_content)

    print(f"OK: {filename}")
    return True

if __name__ == "__main__":
    success = 0
    skip = 0
    error = 0
    for filename, data in SEO_DATA.items():
        result = process_file(filename, data)
        if result:
            success += 1
        elif result is False:
            skip += 1
        else:
            error += 1

    print(f"\nDone: {success} updated, {skip} skipped, {error} errors")
